package Tables;

public class Monster {

    private String mobName;
    private int mobHp;
    private int mobDmg;

    public String getMobName() {
        return mobName;
    }

    public void setMobName(String mobName) {
        this.mobName = mobName;
    }

    public int getMobHp() {
        return mobHp;
    }

    public void setMobHp(int mobHp) {
        this.mobHp = mobHp;
    }

    public int getMobDmg() {
        return mobDmg;
    }

    public void setMobDmg(int mobDmg) {
        this.mobDmg = mobDmg;
    }


}
